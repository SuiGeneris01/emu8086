
org 100h
INCLUDE "EMU8086.INC"

MOV DX, OFFSET MAIN
MOV AH, 9 
INT 21H

MOV DX, OFFSET MSG
MOV AH, 9
INT 21H
CALL SCAN_NUM
MOV RANGE, CX

MOV RES, 0
MOV BX, 1

JMP LABEL

LABEL:
MOV DX, OFFSET MSG2
MOV AH, 9
INT 21H
CALL SCAN_NUM

ADD RES, CX
INC BX

CMP RANGE, BX
JB EXIT
JMP LABEL
       
EXIT:
MOV AX,RES 
LEA SI, MSG3
CALL PRINT_STRING
CALL PRINT_NUM      
ret

RANGE DW ?
RES DW ?
MAIN DB "JUST AN HEADER $"
MSG DB 13,10, "THE RANGE: $"
MSG2 DB 13,10, "NUMBER (+ ENTER KEY) : $"
MSG3 DB 13,10, "RESULT: ", 0

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_PRINT_STRING
 
RET  
