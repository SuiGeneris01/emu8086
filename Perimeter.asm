org 100h

include "emu8086.inc"

MOV     DX, OFFSET MAIN
MOV     AH,9
INT     21H

MOV     DX, OFFSET QUE
MOV     AH,9
INT     21H
CALL    scan_num
MOV     BX,CX

MOV     DX, OFFSET QUES
MOV     AH,9
INT     21H
CALL    scan_num
MOV     AX,CX

CALL    pthis
        db 13,10, "THE PERIMETER OF THE RECTANGLE IS = ", 0
CALL    PERIMETER
CALL    PRINT_NUM

ret
A DW 0
B DW 2
MAIN db "CALCULATES THE PERIMETER OF A RECTANGLE. $"
QUE db 13,10, "ENTER B: $"
QUES db 13,10, "ENTER C: $"

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_PTHIS

PERIMETER PROC
    ADD AX, BX
    MUL B
    RET
    ENDP

E
